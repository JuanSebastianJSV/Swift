import UIKit

var someCharacter:Character = "a"

if someCharacter == "a" {
    print("is an a")
}
// If Statement

// Switch Statement
switch someCharacter{
case "a":
    print("is an a")
case "b":
    print("is a b")
default:
    print("not an a or a b")
}
// we can assume that case is if and default is else
// case: if the value matches the case, then run the codee
// default: if no value that match the case, then run the default code.


